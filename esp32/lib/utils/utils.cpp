float captureMagValues(TFT_eSPI& tft, Adafruit_LIS3MDL& lis3mdl) {
  const int sampleCount    = 100;
  TickType_t xLastWakeTime = xTaskGetTickCount();
  float averageValue       = 0;
  for (int i = 0; i < sampleCount; i++) {
    averageValue += measureMag(tft, lis3mdl);
    vTaskDelayUntil(&xLastWakeTime, pdMS_TO_TICKS(SAMPLE_RATE_MS));
  }
  return averageValue / sampleCount;
}

float measureMag(TFT_eSPI& tft, Adafruit_LIS3MDL& lis3mdl) {
  sensors_event_t event;
  lis3mdl.getEvent(&event);
  char str[16];
  tft.setTextSize(2);

  if (event.magnetic.y >= 100 || event.magnetic.y <= -100) {
    sprintf(str, "%d", (int)event.magnetic.y);
  } else {
    sprintf(str, "%.1f", event.magnetic.y);
  }

  tft.fillRect(0, TFT_HEIGHT - 40, TFT_WIDTH, 40, TFT_BLACK);
  tft.drawCentreString(str, TFT_WIDTH / 2, TFT_HEIGHT - 40, 2);

  return event.magnetic.y;
}

