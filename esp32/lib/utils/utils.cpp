TriggerStatus getTriggerStatus(TFT_eSPI& tft, float sensorReading, float neutralPoint, float triggerPoint) {
  if ((neutralPoint < triggerPoint && sensorReading >= triggerPoint) ||
      (neutralPoint > triggerPoint && sensorReading <= triggerPoint)) {
    tft.fillCircle(TFT_WIDTH / 2, TFT_HEIGHT / 2, 10, TFT_GREEN);
    return CLOSE_TO_TRIGGER;
  } else if ((neutralPoint < triggerPoint && sensorReading <= neutralPoint) ||
             (neutralPoint > triggerPoint && sensorReading >= neutralPoint)) {
    tft.fillCircle(TFT_WIDTH / 2, TFT_HEIGHT / 2, 10, TFT_BLUE);
    return CLOSE_TO_NEUTRAL;
  } else if ((neutralPoint < triggerPoint && sensorReading > neutralPoint && sensorReading < triggerPoint) ||
             (neutralPoint > triggerPoint && sensorReading < neutralPoint && sensorReading > triggerPoint)) {
    tft.fillCircle(TFT_WIDTH / 2, TFT_HEIGHT / 2, 10, TFT_RED);
    return IN_BETWEEN;
  } else {
    tft.fillCircle(TFT_WIDTH / 2, TFT_HEIGHT / 2, 10, TFT_LIGHTGREY);
    return OUTSIDE_RANGE;
  }
}
